package desafios.desafioSeis.util;

public class Calculadora {

	public static float calcularTamanho(float largura, float comprimento) {
		return largura*comprimento;
	}
	
	
	public static float calcularQtdCeramica(float tamanhoTotal, float tamanhoCeramica) {
		
		return tamanhoTotal/tamanhoCeramica;
	}
	
}
